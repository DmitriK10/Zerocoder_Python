# AZ01 Выведите первые 5 строк данных, чтобы получить представление о структуре данных.
# Выведите информацию о данных (.info()) и статистическое описание (.describe()).

import pandas as pd

# Загрузка данных из CSV-файла
df = pd.read_csv('penguins.csv')

# 1. Вывод первых 5 строк данных
print("1. ПЕРВЫЕ 5 СТРОК ДАННЫХ:")
print(df.head())
print("\n" + "="*80 + "\n")

# 2. Вывод информации о данных
print("2. ИНФОРМАЦИЯ О ДАННЫХ (.info()):")
print(df.info())
print("\n" + "="*80 + "\n")

# 3. Вывод статистического описания числовых столбцов
print("3. СТАТИСТИЧЕСКОЕ ОПИСАНИЕ (.describe()):")
print(df.describe())
print("\n" + "="*80 + "\n")

# 4. Статистика для всех столбцов (включая нечисловые)
print("4. СТАТИСТИЧЕСКОЕ ОПИСАНИЕ ДЛЯ ВСЕХ СТОЛБЦОВ (include='all'):")
print(df.describe(include='all'))
print("\n" + "="*80 + "\n")

