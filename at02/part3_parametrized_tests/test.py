import pytest
from main import check


def test_check():
    """Простой тест для четного числа"""
    assert check(6) == True


def test_check2():
    """Простой тест для нечетного числа"""
    assert check(3) == False


@pytest.mark.parametrize("number, expected", [
    (2, True),   # 2 - четное, ожидаем True
    (5, False),  # 5 - нечетное, ожидаем False
    (0, True),   # 0 - четное, ожидаем True
    (56, True),  # 56 - четное, ожидаем True
    (-3, False), # -3 - нечетное, ожидаем False
])
def test_check_with_param(number, expected):
    """
    Параметризованный тест
    
    Args:
        number: Число для проверки
        expected: Ожидаемый результат (True/False)
    
    Декоратор @pytest.mark.parametrize позволяет запустить 
    эту функцию несколько раз с разными значениями параметров.
    Каждый кортеж в списке - отдельный тестовый случай.
    """
    assert check(number) == expected


# Комментарий:
# Параметризованные тесты позволяют:
# 1. Уменьшить дублирование кода
# 2. Легко добавлять новые тестовые случаи
# 3. Получать детальные отчеты для каждого набора данных
# 4. Упростить поддержку тестов

# Чтобы увидеть ошибку, можно изменить один из ожидаемых результатов:
# Например, заменить (-3, False) на (-3, True)
# При запуске теста будет показано, для какого набора данных произошла ошибка