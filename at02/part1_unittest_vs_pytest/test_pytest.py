import pytest


def test_add():
    """Тест сложения с использованием PyTest"""
    assert 1 + 1 == 2  # Проверяем, что 1+1 равно 2


def test_subtract():
    """Тест вычитания с использованием PyTest"""
    assert 3 - 2 == 1  # Проверяем, что 3-2 равно 1


# Комментарий:
# Код с PyTest выглядит проще и короче, чем с UnitTest
# Не нужно создавать классы и использовать специальные методы
# Используются обычные функции и ключевое слово assert