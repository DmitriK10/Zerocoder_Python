def is_palindrome(s):
    """
    Проверяет, является ли строка палиндромом
    
    Args:
        s (str): Строка для проверки
    
    Returns:
        bool: True если строка палиндром, False если нет
    """
    return s == s[::-1]  # Сравниваем строку с её перевернутой версией


# Объяснение конструкции s[::-1]:
# [::-1] - это срез (slice) строки с шагом -1
# Это означает: взять всю строку от начала до конца в обратном порядке
# Пример: "madam"[::-1] = "madam"