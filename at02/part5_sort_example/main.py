def sort_list(numbers):
    """
    Сортирует список чисел по возрастанию
    
    Args:
        numbers (list): Список чисел для сортировки
    
    Returns:
        list: Отсортированный список
    """
    return sorted(numbers)  # Используем встроенную функцию sorted


# Функция sorted возвращает новый отсортированный список
# Не изменяет исходный список (в отличие от метода list.sort())