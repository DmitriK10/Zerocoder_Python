#5. Написать конвертер валюты. Эта программа будет конвертировать введённую пользователем сумму в другую валюту. Курс валюты можно ввести самостоятельно и один раз.
exchange_rate = 90.5  # Фиксированный курс доллара
usd_amount = float(input("Введите сумму в USD: "))  # Сумма для конвертации

rub_amount = usd_amount * exchange_rate  # Конвертация
print(f"{usd_amount} USD = {rub_amount:.2f} RUB")  # Вывод с округлением до копеек
print()  # Пустая строка