# Менеджер задач
# Задача: Создай класс `Task`, который позволяет управлять задачами (делами). У задачи должны быть атрибуты: описание задачи,
# срок выполнения и статус (выполнено/не выполнено). Реализуй функцию для добавления задач, отметки выполненных задач и
# вывода списка текущих (не выполненных) задач.
class TaskManager:
    def __init__(self):
        # Список для хранения задач в формате [описание, срок, статус]
        self.tasks = []

    def add_task(self, description, deadline):
        # Добавляем задачу: описание, срок, статус (False = не выполнено)
        self.tasks.append([description, deadline, False])

    def mark_done(self, description):
        # Отмечает задачу как выполненную по описанию
        for task in self.tasks:
            # Если найдена задача с указанным описанием
            if task[0] == description:
                # Помечаем как выполненную
                task[2] = True

    def show_current_tasks(self):
        #Показывает список невыполненных задач
        print("Текущие задачи:")
        for task in self.tasks:
            # Проверяем статус выполнения
            if not task[2]:
                # Выводим описание и срок
                print(f"- {task[0]} (до {task[1]})")


# Создаем менеджер задач
manager = TaskManager()

# Добавляем задачи
manager.add_task("Купить продукты", "2023-11-25")
manager.add_task("Сходить в бассейн", "2023-12-10")
manager.add_task("Сделать задание", "2023-11-30")

# Отмечаем одну задачу выполненной
manager.mark_done("Купить продукты")
#manager.mark_done("Сходить в бассейн")

# Показываем невыполненные задачи
manager.show_current_tasks()