# Пузырьковая сортировка (Bubble Sort)
# 1. Инициализируем массив
mas = [5, 7, 4, 3, 8, 2]

# 2. Определяем количество элементов в массиве
n = 6

# 3. Внешний цикл: количество проходов (n-1)
for run in range(n - 1):
    # 4. Внутренний цикл: сравниваем соседние элементы, исключая уже отсортированные
    for i in range(n - 1 - run):
        # 5. Если текущий элемент больше следующего, меняем их местами
        if mas[i] > mas[i + 1]:
            mas[i], mas[i + 1] = mas[i + 1], mas[i]

# 6. Выводим отсортированный массив
print(mas)