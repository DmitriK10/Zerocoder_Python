# Сортировка вставками (Insertion Sort)
def insert_sort(arr):
    # 1. Начинаем со второго элемента, так как первый считается отсортированным
    for i in range(1, len(arr)):
        # 2. Сохраняем текущий элемент для вставки
        key = arr[i]

        # 3. Индекс предыдущего элемента для сравнения
        j = i - 1

        # 4. Сдвигаем все элементы больше key вправо, чтобы освободить место
        while j >= 0 and arr[j] > key:
            arr[j + 1] = arr[j]
            j -= 1

        # 5. Вставляем key на правильную позицию
        arr[j + 1] = key

# 6. Тестируем алгоритм
a = [-3, 5, 0, -8, 1, 10]
insert_sort(a)
print(a)  # [-8, -3, 0, 1, 5, 10]